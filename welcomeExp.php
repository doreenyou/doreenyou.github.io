  	<div class="content">
		  <p>Aiming to improve newcomers experience in Sønderborg, in this 8-weeks project we used techniques such as “Cultural Probes” to avoid taken-for-granted assumptions and to truly understand the difficulties that newcomers need to face when they come to this new city. Our starting point considers that there is no better way of developing meaningful solutions without inviting the “users” to think and brainstorm together.</p>
      <p>The project counts with two partners-organizations that share interests and thereby cooperate with the team to develop a solution that is, beyond meaningful for the user, possible to be implemented in a short-term time. </p>
      <h4>EXPLORING</h4>
      <p>Different Service Design methods and tools were used to get inspired for coming up with different new ideas or concepts. The methods we used were: story-telling, in which all group members shared their stories of being a newcomer here in Sønderborg. By doing workshops with a variety of users, we developed methods such as personas and customer journey map, so that we could create groups of interests and get some insights and conclusions that could lead us to the following step of creating new concepts.</p>
