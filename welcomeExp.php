      <h4>CONCEPTING</h4>
      <p>After using several methods and tools, we came up with four main concepts or ideas that could help to improve the experience of newcomers here in Sønderborg, which is the main goal this project. The mentioned concepts would be a Welcome Kit, a Funny Dictionary, Public Displays and City Explorer.</p>
      <div class="inline"><img src="projects/welcomeExp/019.jpg"><img src="projects/welcomeExp/020.jpg"><img src="projects/welcomeExp/021.jpg"><img src="projects/welcomeExp/022.jpg"><img src="projects/welcomeExp/023.jpg"></div>
     
      <h4>The FINAL Concept</h4>
      <p>The final concept brings an interactive experience via a labyrinth in real scale placed downtown. The analogy was meant to make people find themselves in the Labyrinth of Sønderborg full of interesting and interactive attractions to be found and tried out. It also considers different levels of interaction: for people who will just passed by, notice it but not really interact, for people who want to have a non-committed level of interaction by only writing in the wall outside and finally for those ones who will take some time to get inside, trying new things and meeting new people. </p>
      <p>The labyrinth, so called "Sønderborg Welcome Experience", is composed by 5 thematic rooms made out of game and interactive activities: “Interactive Wall”, “Do it yourself Kit”, “ City Explorer”, “Danish Flavors”, “Danish Language”. The concept of each room intends to merge most of the isolated ideas we presented in the “concept cards”. It also clearly brings back the opportunities we identified when we were conducting cultural probes activities with user. The rooms also provides opportunities to advertise interesting projects happening in the city.ers, the expectations of the partners and provides a meaningful experience to the user.</p>
