    <h4>Project goal</h4>
		<p>The current system for sending travel and expense reports is very difficult to use, especially when user tries to attach the expense receipts, they have to scan the receipts at first, then add to the report. For Mac users, it’s even a more “impossible mission”. Thus, this app aims at helping user upload pictures of receipts easily to the travel report system.</p>

