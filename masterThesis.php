		<h5>Fieldwork with design artefacts</h5>
		<p>The user study was done inside the transition area in the airport. By bringing the design to the “real context”, the participants could relate their on-going journey with the design. At last, 15 interviews including 12 individual interviews and 3 group interviews were carried out in 8 days. The age of participants ranged from 23-55 years, with gender equivalence in consideration. Normally passengers didn’t have a long time to participate in the study, so one interview on average lasted for 15-20 minutes. 
Steps of the semi-structure interviews:
1. Introduce the research purpose
2. Get information of the present travel by using a journey map I made for the participants as a self-documentation, so they could write down the details of their journey at each touchpoint. (Usually the journey map is done among the design/research team, but here I used it as an inquiry tool at the beginning for participants to recall their journey from leaving home to waiting for boarding.)
3. Engage informants in the three design artefacts session 
4. Go in depth if needed and permitted</p>
