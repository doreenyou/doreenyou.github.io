  		<h4>Project Background</h4>
		<p>Navigaid is a 3-years project within the cooperation between multiple stakeholders in design, technology, service and aviation area. The goal was to develop a new airport luggage handling trolley with an embedded tablet.</p>
		<p>My design agenda was on the interaction design between passengers and this new trolley, especially in terms of the software system. I combined product design with design ethnographically inspired fieldwork as my main design research activities.</p>

		<h4>Design activities</h4>
		<img src="projects/masterThesis/1.png">
		<h5>Initial studies: to explore the design context and to gain design requirements</h4>
		<h6>Value Network Mapping</h6>
		<p>I mapped out all the stakeholders and value exchange that influence passengers’ experience in the airport to see how the implementation of luggage handling trolley with an informative system would intervene the value flow.</p>
		<img src="projects/masterThesis/2.png">

		<h6>Desk research: to gather and to analyse existing empirical data</h6>
		<p>Based on the former research results from 400 surveys through the Internet and 57 interviews in CPH Airport and Keflavik Airport conducted by CPH Inventures, I generated some insights from KJ Method as some implication for the focus of the system design. These implications contributed to the design inspiration and requirements in the later-on prototype design, such as offering a friendly start page, adding some social features, and providing facilities information. </p>
		<div class="inline"><img src="projects/masterThesis/3.png"><img src="projects/masterThesis/11.png"></div>

		<h6>Field observation: to study the present signage arrangement</h6>
		<p>For capturing relevant visual accounts as the ethnographic research background, I observed inside the transit area to collect touchpoints (i.e. the places that interaction happens between users and service) evidences for way-finding. </p>
		<div class="inline"><img src="projects/masterThesis/4.png"><img src="projects/masterThesis/5.png"><img src="projects/masterThesis/6.png"></div>

		<h6>Interview: to know about passengers’ purchasing behaviours </h6>
		<p>I conducted interviews inside the design company with employees who had all been traveling through different airports for times, to develop an understanding of their traveling experience in different airports‘ transit area compared to CPH Airport. </p>

		<h5>Artefacts design</h4>
		<p>In order to reveal the human aspect of perceiving this trolley, I decided to go beyond usability engineer scope, using ethnography and design visions to plump design work and to extend the scope for future contexts. </p>
		<h6>Design the present: Prototype the app</h6>
		<p>I built an interactive tablet application, and a connection between the tablet and luggage handling trolley, to test the usability.</p>

