    <h4>Purpose</h4>
		<p>Tangible material can make business modeling more accessible than post-it brainstorming exercises in board rooms for managers and marketing. As designers, we aimed at facilitating new business development with the help of tangible mechanisms to bring tacit knowledge and hidden interests to the table and help all involved parties succeed in their undertaking. In this project our goal was to develope a tangible business model as tools to foster experiments and conversations in Polypower, a company with an exciting technology that they want to turn into products.</p>

    <h4>Background</h4>
    <p>Danfoss Polypower produces a DEAP material that can be used as a sensor. They are looking for needs that could be satisfied by this sensor in order to turn it into an innovative product. However, there is a clear division between Polypower's expertise and their customers' expertise. Therefore they need to find suitable partners to realize the product.</p>

    <h4>Business Model Canvas</h4>
    <p>After meeting with the client Danfoss Polypower, we made a Business Model Canvas to explore the potential channels, partners/customers and end users.</p>
