    <h4>Project Background</h4>
		<p>Several new offices had been opened globally, and their open workspace setup created the needs for the employees to locate their colleagues and to look up hub-related information. This project aimed at releasing an app to support this open workspace culture.</p>

    <h4>Concepting</h4>    
    <p>Within very limited time scope and budget, we had to come up with a prototype before having the oppotunity to conduct field research. Thus we used an ideation workshop to brainstorm with the developers, UX, and business to select the most valuable concepts under the open workspace setup. A POC app was developed afterwards for user validation. </p>

    <h4>Field Research</h4>
    <p>I visited the London Hub office to understand the open workspace culture and to observe how people interact with the office facilities. Through observation and inquiries, I found out quite some realities and pain points that the project didnâ€™t realize before, such as the strong culture policy of encouraging people using social space instead of meeting rooms, the blackboards at the entrance with all the social info and hub info, more visitors than local workers came to the office, etc. All the findings helped to refine the design requirements. </p>
    <img src="projects/legoHubs/1.png">

    <h4>User interview (local+online)</h4>
    <p>In London hub, we interviewed 12 recruited local users and visitors to understand their real needs and wishes, which I specified to them to not only limit in the scope of an app. I brought the app prototype to verify the concepts as well as to look for other ideas through conversation. During the interviews with new and old employees, we found out their work habits under different contexts such as using a phone booth without booking, forgetting to press "End meeting" on the tablet on the meeting room doors, leaving laptops behind while walking around or going for lunch and using their phones instead, etc. In the mean time, we got many ideas such as "after booked a meeting room in the app, within 5-10 minutes before the meeting starts, one has to confirm on the meeting room iPad", "new joins' info can be updated in the app news", "detailed help for visitors to find the hub office for the first time", etc.

    I also interviewed 5 Singapore users online to understand their office setup, their culture and work routine.
    </p>

    <h4>Final Result</h4>
    <p>After communicated all the findings to the development team, we made some new design decisions. The new app has made a significant improvement to solve the pain points and to meet actual needs of both the organization and users. It became a huge hit in the London and Singapore offices after released. </p>
    
