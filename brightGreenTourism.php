    <h4>Purpose</h4>
		  <p>The purpose of Bright Green Tourism has been to discover ways to increase public interest in visiting Sønderborg 
      with a unique focus on connecting both tourism and sustainable energy.</p>
      <p>We adopted Design Charrette as "an intensive planning session, where citizens, designers and stakeholders collaborate on a vision for development. It provides a forum for ideas and offers the unique advantage of giving immediate feedback to the designers. More importantly, it allows everyone who participates to be a mutual author of the plan." (<a title="the" href="http://www.tndtownpaper.com/what_is_charrette.htm">The Town Paper</a>)</p>
      <p>Collaborating with SET Sønderborg and Project Zero, this Design Charette expanded 
      to include other stakeholders inviting input and feedback from many other local businesses such as: the Sønderborg Municipality, SDU Sønderborg and Kolding, 
      Universe, Servodan, Joltech, Shopping Hovedstaden and iFabrikken. </p>
    
      <h4>Agile Design Process </h4>
      <p>As a 2-week project, we need to make out a service plan in a short time. Everyday we had morning briefing and reflection - feedback process. </p>
