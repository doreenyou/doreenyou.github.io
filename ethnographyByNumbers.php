  		<h4>Project goal</h4>
		<p>This project aimed to combine ethnography studies with quantitative analysis  in a discussion related to whether freezers in the supermarkets should have lids. </p>

		<h4>Research question</h4>
		<p>Lids can be energy-efficient and therefore save money, but how are the customers affected by lids? Is their shopping behavior influenced by the fact that they need to open a lid in order to have access to the products? Is there a difference between the supermarket with lids and the supermarkets without?</p>

		<h4>Process</h4>
		<p>We studied shoppers in three supermarkets in Denmark, two with lids and one without lids. A video extract of each supermarket was used to analyse the customers’ behavior. Then we created data visualizations and used them as basis for additional interviews with shoppers and supermarket personnel. </p>
