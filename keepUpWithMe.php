  	<h2>Keep Up With Me!</h2>
    <h3>September - December, 2013</h3>
    
  	<div class="content">
		<p>The design purpose is to address an unspoken dining rule, which is respecting other’s dinning habits – in our case the pace of eating. Instead of over-exaggerating user’s faults by judging and pointing on him, we tried to let this come in a more natural way, so we created a synchronising dinning experience in order to coordinate the pace dinners have while eating.</p>
        <p>This design inspired by critical design (Dunne, 1999) has potentials to generate uncomfortable feelings to some users. They might feel “annoyed” temporarily (Benford et al, 2012). But the design intention is to provide the possibility for them to reflect and empathize during or after interaction with our prototype.</p>
        <p>In the interaction, people need to adjust their own dinning pace in order to keep a balance. The observation of objective action requires a reflection on intrinsic action.</p>

