        <p>During user testings people were found to be confused regarding the principle function of the prototype before before certain instructions were given to them. Most of the people thought that it was a simple scale prototype. The manual control was considered as appendix operation. In the functional prototype user testing, people were more immersed in the synchronous dinning experience. They also felt empathized with the experience of eating in a non respectful way (when it comes to eating speed). Thus this design that combined electronics and mechanics revealed the original mission by leading users to finish desired interaction task. A synchronising dining experience is created in both coercive and seductive way.</p>
    
