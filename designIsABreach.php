  	<h2>Design Is A Breach</h2>
    <h3>September 17 – October 3, 2012</h3>
  	<div class="content">
    <h4>Purpose</h4>
		<p>The context in which many products and services are used is increasingly social. We interact with technology for both work and pleasure in 
		all sorts of different environments, with and around other people. Consequently, an ability for designers to create and to support new social relationships 
		becomes increasingly valuable. But in order for such relationships, we first need to know something about how people ordinarily behave. In this project we 
		explored how the staging of interventions could reveal the hidden social order of human relationships and ultimately be employed as a design tool, 
		to create new social relationships in the public space.</p>

    <h4>Social interaction design</h4>
		<p>We did an experiment to explore how to generate the empathy between stangers. We designed a simple set-up with an obtrusive impact - a physical obstacle forcing people to stop walking and to collaborate, but most importantly it sparked conversations between strangers. A practical example of William Whyte’s notion of triangulation.
		</p>

