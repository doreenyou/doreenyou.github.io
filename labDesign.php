    <h4>Purpose </h4>
		<p>Design research as a field blends methodologies from several disciplines, such as the social sciences, cognitive psychology, and the arts. In this project, three design research traditions that cover methodological directions in current leading research communities: the lab, the field, and the showroom, were explored. As a member of lab group I worked on the Greed and Charity topic. We designed two vending machines to develop knowledge about designing for experiences of greed and charity.</p>
    <h4>Research question </h4>
    <p>Does physical effort make people more charitable or greedy? </p>
    <h4>Hypothesis </h4>
    <p>The more physical effort a vending machine requires, the less charitable is the users’ behavior </p>
    <h4>Prototyping </h4>
    <p>To find out whether physical effort makes people more charitable or greedy, we built two different vending machines. One has a simple interface with two coin slots, one for the product and one for donation.</p>
    <p>The second machine has the same to slots, but in order to get a soda, a miniature soda can must be moved through a maze. Therefore, this vending machine requires more physical effort then the first one. Considering our hypothesis, users would behave less charitable when using the second machine.</p> 
    <div class="inline"><img src="projects/labDesign/002.jpg"><img src="projects/labDesign/001.jpg"></div>
    <h4 class="description">Machine 1: Simple Interface                         Machine 2: Arduous Interface </h4>   

    <p>We used the same design for both machines, including the design of the money slots, the can dispenser and the logos in order not to influence people’s behavior. To be able to analyze people’s behavior and categorize it as greedy or charitable, we used candy coins as money for them to use the vending machines. We gave five coins to the user with the instructions to spend them however they wanted to on one of the machines. They had three options: </p>
    <p>1. To buy coke 
    <br>2. To donate the money 
    <br>3. To keep the coins </p>
    
    <div class="inline"><img src="projects/labDesign/003.jpg"><img src="projects/labDesign/004.jpg"></div>
    <h4 class="description">Coins used as payment                              Product used                </h4>

    <p>Since the price for one coke was two coins, the most coins one user could get out of the machine were two. Then they could donate or keep the last coin. After using the first vending machine, the users got five more coins to use the second machine. They could not use the coins that they might have kept at the machine before. We switched the order in which the users used the machines regularly.</p>
    <h4>The Set Up</h4>
    <p>The participants were always able to see only one machine at a time</p>
    
    <div class="inline"><img src="projects/labDesign/005.jpg"><img src="projects/labDesign/006.jpg"></div>
    <h4 class="description">First set up                                                           Second set up</h4>

    <h4>Data/Analysis</h4>
    <p><b>What is greed?</b> “a selfish and excessive desire for more of something than is needed” (Merriam-Webster Online)
    <br>Based on this definition we divided people’s behavior into three categories:
    <br><b>Greedy</b>: Taking more than one coke per machine
    <br><b>Neutral</b>: Taking one coke and keeping at least one coin, keeping all coins
    <br><b>Charitable</b>: Taking no coke and donating at least one coin, taking one coke and donating the rest</p>


    <p>The data show the users’ behavior when using each of the machines - they are separated into machine 1, machine 2 and the order the users used the machines.</p>
    <p>Users’ first use of a machine:
    <br>- No charitable behavior in machine 2 while almost half of all people using machine 1 first were charitable
    <br>- 5 % less greedy in machine 2 than in machine 1
    <br>==> Supports the hypothesis that the machine with more physical effort results in less charitable behavior, however, less charitable behavior does not automatically mean more greedy behavior</p>
    <p>Users’ second use of a machine:
    <br>- 13 % less charitable behavior in machine 2 compared to machine 1
    <br>==> Supports the hypothesis
    <br>- 33 % more charitable than in the first use of machine 1</p>
    <p>In general, people tend to be less greedy and also more charitable at the second machine they use than they were at the first one, independent of which machine they used first.</p>
